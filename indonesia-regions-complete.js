// ===================================================================
// ðŸŽ¯ UTILITY FUNCTIONS
// ===================================================================

class IndonesiaRegions {
    
    // Get all provinces
    static getProvinces() {
        return Object.keys(INDONESIA_REGIONS).sort();
    }
    
    // Get kabupaten/kota by province
    static getKabupaten(provinsi) {
        return INDONESIA_REGIONS[provinsi] || [];
    }
    
    // Get total count
    static getTotalProvinces() {
        return Object.keys(INDONESIA_REGIONS).length;
    }
    
    static getTotalKabupaten() {
        return Object.values(INDONESIA_REGIONS)
            .reduce((total, kabupaten) => total + kabupaten.length, 0);
    }
    
    // Validation
    static isValidProvinsi(provinsi) {
        return Object.keys(INDONESIA_REGIONS).includes(provinsi);
    }
    
    static isValidKabupaten(provinsi, kabupaten) {
        const kabupatens = INDONESIA_REGIONS[provinsi] || [];
        return kabupatens.includes(kabupaten);
    }
    
    // Search functions
    static searchProvinsi(searchTerm) {
        return Object.keys(INDONESIA_REGIONS)
            .filter(provinsi => 
                provinsi.toLowerCase().includes(searchTerm.toLowerCase())
            );
    }
    
    static searchKabupaten(searchTerm) {
        const results = [];
        Object.entries(INDONESIA_REGIONS).forEach(([provinsi, kabupaten]) => {
            kabupaten.forEach(kab => {
                if (kab.toLowerCase().includes(searchTerm.toLowerCase())) {
                    results.push({ provinsi, kabupaten: kab });
                }
            });
        });
        return results;
    }
    
    // Generate dropdown options
    static generateProvinsiOptions() {
        return this.getProvinces().map(provinsi => ({
            value: provinsi,
            label: provinsi
        }));
    }
    
    static generateKabupatenOptions(provinsi) {
        return this.getKabupaten(provinsi).map(kabupaten => ({
            value: kabupaten,
            label: kabupaten
        }));
    }
}

